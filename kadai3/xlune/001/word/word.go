// Issue ワードを履歴に発行する
func Issue() (string, error) {
	totalCount := len(list)
	usedCount := len(history)
	randCount := totalCount - usedCount
	if randCount <= 0 {
		return "", errors.New("no available words")
	}
	// 有効数内からランダム値生成
	t := time.Now().UnixNano()
	rand.Seed(t)
	index := rand.Intn(randCount)
	// ランダム値を有効インデックス化
	h := make([]int, len(history))
	copy(h, history)
	sort.Ints(h)
	for _, i := range h {
		if i <= index {
			index++
		} else {
			break
		}
	}
	// 履歴追加してワードを正常返却
	history = append(history, index)
	return GetLatest(), nil
}

// GetLatest 直近の発行ワードを取得
func GetLatest() string {
	if len(history) == 0 {
		return ""
	}
	latestIndex := history[len(history)-1]
	return list[latestIndex]
}

// ClearHistory ワード発行履歴をクリア
func ClearHistory() {
	history = []int{}
}

// CountHistory ワード発行履歴数を取得
func CountHistory() int {
	return len(history)
}

// CountTotal 総ワード数
func CountTotal() int {
	return len(list)
}

// CheckLatest 入力文字列と直近の発行文字列の同一チェック
func CheckLatest(input string) bool {
	if input == "" {
		return false
	}
	return input == GetLatest()
}
